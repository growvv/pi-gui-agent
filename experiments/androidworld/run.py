"""Run one AndroidWorld worker from a generated JSON configuration."""

from __future__ import annotations

import argparse
import os
from pathlib import Path
import subprocess
import time
from typing import Callable

from android_world import checkpointer, registry, suite_utils
from android_world.env import adb_utils, env_launcher, tools
from android_env.components import adb_controller
from android_env.wrappers import a11y_grpc_wrapper
from android_world.env.setup_device import apps as setup_apps
from android_world.env.setup_device import setup as setup_device
from android_world.utils import app_snapshot, file_utils

from .config import load_worker_config
from .factory import create_agent
from .state import StateTrackingCheckpointer, write_worker_state


def parse_args() -> argparse.Namespace:
  parser = argparse.ArgumentParser(description=__doc__)
  parser.add_argument('config', help='worker JSON generated by the parallel runner')
  return parser.parse_args()


def main() -> None:
  config = load_worker_config(parse_args().config)
  state_path = Path(config.runtime.checkpoint_dir).parent / 'state.json'
  max_steps = getattr(config.agent, 'max_steps', 100)
  episodes = []
  tracking_checkpointer = None
  write_worker_state(
      state_path, config.suite.tasks, config.suite.combinations, episodes,
      max_steps, 'running',
  )
  os.environ.setdefault('GRPC_VERBOSITY', 'ERROR')
  os.environ.setdefault('GRPC_TRACE', 'none')
  _increase_activity_start_timeout()
  _configure_download_cache()
  _stabilize_camera_setup()
  _stabilize_chrome_setup()
  _stabilize_clipper_setup()
  _stabilize_contacts_setup()
  _stabilize_simple_sms_setup()
  _stabilize_vlc_setup()
  restore_accessibility_route = _route_accessibility_over_adb(
      config.runtime.adb_path, config.runtime.console_port,
  )

  runtime = config.runtime
  suite_settings = config.suite
  setup_marker = Path('/root/.android/avd/.pi-gui-androidworld-setup-v1')
  perform_setup = suite_settings.setup_mode == 'always' or (
      suite_settings.setup_mode == 'auto' and not setup_marker.is_file()
  )
  environment = env_launcher.load_and_setup_env(
      console_port=runtime.console_port,
      emulator_setup=perform_setup,
      adb_path=runtime.adb_path,
      grpc_port=runtime.grpc_port,
  )
  try:
    # Device setup invokes adb root after the wrapper is initialized, which
    # clears reverse sockets. Re-establish the route at the task boundary.
    restore_accessibility_route()
    _stabilize_sms_reads()
    if perform_setup:
      setup_marker.write_text('setup completed\n', encoding='utf8')
    task_registry = registry.TaskRegistry()
    suite = suite_utils.create_suite(
        task_registry.get_registry(family=suite_settings.family),
        n_task_combinations=suite_settings.combinations,
        seed=suite_settings.seed,
        tasks=list(suite_settings.tasks) or None,
        use_identical_params=suite_settings.fixed_task_seed,
        env=environment,
    )
    suite.suite_family = suite_settings.family
    agent = create_agent(
        config.agent.name,
        environment,
        workspace_dir=runtime.workspace_dir,
        adb_path=runtime.adb_path,
        serial=f'emulator-{runtime.console_port}',
        timeout_seconds=suite_settings.timeout_seconds,
        thinking=config.agent.thinking,
        settle_ms=suite_settings.settle_ms,
        session_dir=runtime.session_dir,
        action_budget_multiplier=suite_settings.action_budget_multiplier,
        min_actions=suite_settings.min_actions,
        max_model_tokens=suite_settings.max_model_tokens,
        learning=config.agent.learning,
        max_steps=max_steps,
        provider=config.agent.provider,
        model=config.agent.model,
        openclaw_model=config.agent.openclaw_model,
        enable_ledger_tool=config.agent.enable_ledger_tool,
        disable_ledger_tool=config.agent.disable_ledger_tool,
    )
    tracking_checkpointer = StateTrackingCheckpointer(
        checkpointer.IncrementalCheckpointer(runtime.checkpoint_dir),
        lambda saved: write_worker_state(
            state_path, config.suite.tasks, config.suite.combinations, saved,
            max_steps, 'running',
        ),
    )
    episodes = suite_utils.run(
        suite,
        agent,
        checkpointer=tracking_checkpointer,
        demo_mode=False,
    )
    write_worker_state(
        state_path, config.suite.tasks, config.suite.combinations, episodes,
        max_steps, 'completed',
    )
    suite_utils.process_episodes(episodes, print_summary=True)
  except Exception:
    saved_episodes = (
        tracking_checkpointer.episodes
        if tracking_checkpointer is not None else episodes
    )
    write_worker_state(
        state_path, config.suite.tasks, config.suite.combinations, saved_episodes,
        max_steps, 'failed',
    )
    raise
  finally:
    environment.close()


def _increase_activity_start_timeout() -> None:
  """Triple the ADB timeout for activity starts that wait for completion."""
  controller_class = adb_controller.AdbController
  if getattr(controller_class.execute_command, '_pi_gui_start_timeout', False):
    return
  original = controller_class.execute_command

  def execute_command(self, args, timeout=None, device_specific=True):
    if len(args) >= 4 and args[:3] == ['shell', 'am', 'start'] and '-W' in args:
      base_timeout = self._config.default_timeout if timeout is None else timeout
      timeout = base_timeout * 3
    return original(
        self, args, timeout=timeout, device_specific=device_specific,
    )

  execute_command._pi_gui_start_timeout = True
  controller_class.execute_command = execute_command


def _route_accessibility_over_adb(
    adb_path: str, console_port: int,
) -> Callable[[], None]:
  """Route the emulator accessibility server back into its worker container."""
  wrapper_class = a11y_grpc_wrapper.A11yGrpcWrapper
  if getattr(wrapper_class, '_pi_gui_adb_reverse', False):
    return wrapper_class._pi_gui_restore_route
  original_init = wrapper_class.__init__
  original = a11y_grpc_wrapper.A11yGrpcWrapper._configure_grpc
  original_set_root = adb_utils.set_root_if_needed
  serial = f'emulator-{console_port}'
  active_wrapper = None

  def configure(wrapper) -> None:
    port = wrapper.get_port()
    subprocess.run(
        [adb_path, '-s', serial, 'reverse', f'tcp:{port}', f'tcp:{port}'],
        check=True, capture_output=True, text=True,
    )
    original(wrapper)
    subprocess.run(
        [
            adb_path, '-s', serial, 'shell', 'am', 'broadcast',
            '-a', 'accessibility_forwarder.intent.action.SET_GRPC',
            '--es', 'host', '127.0.0.1', '--ei', 'port', str(port),
            '-n', ('com.google.androidenv.accessibilityforwarder/'
                   'com.google.androidenv.accessibilityforwarder.FlagsBroadcastReceiver'),
        ],
        check=True, capture_output=True, text=True,
    )
    # A relaunch restarts the forwarder service. Under parallel cold-start
    # load it can accept the broadcast before it is ready to send a11y trees.
    time.sleep(5.0)

  def initialize(wrapper, *args, **kwargs) -> None:
    nonlocal active_wrapper
    original_init(wrapper, *args, **kwargs)
    active_wrapper = wrapper
    configure(wrapper)

  def restore_route() -> None:
    if active_wrapper is None:
      raise RuntimeError('Accessibility wrapper was not initialized during setup.')
    configure(active_wrapper)

  original_setup_app = setup_device.setup_app

  def setup_app_with_route(app, env) -> None:
    # APK installation immediately precedes this call and can restart adbd.
    # Restore before app onboarding, which relies on accessibility clicks.
    restore_route()
    original_setup_app(app, env)
    if getattr(app, 'app_name', None) != 'vlc':
      return
    _ensure_vlc_database(env)

  def set_root_and_restore_route(controller, *args, **kwargs):
    result = original_set_root(controller, *args, **kwargs)
    if active_wrapper is not None:
      configure(active_wrapper)
    return result

  wrapper_class._configure_grpc = configure
  wrapper_class.__init__ = initialize
  wrapper_class._pi_gui_adb_reverse = True
  wrapper_class._pi_gui_restore_route = restore_route
  adb_utils.set_root_if_needed = set_root_and_restore_route
  setup_device.setup_app = setup_app_with_route
  return restore_route


def _ensure_vlc_database(env) -> None:
  """Complete both old and current VLC onboarding flows before snapshotting."""
  database = '/data/data/org.videolan.vlc/app_db/vlc_media.db'
  package = adb_utils.extract_package_name(adb_utils.get_adb_activity('vlc'))
  controller = tools.AndroidToolController(env=env.controller)
  for attempt in range(20):
    if file_utils.check_file_exists(database, env.controller):
      adb_utils.close_app('vlc', env.controller)
      app_snapshot.save_snapshot('vlc', env.controller)
      return
    if attempt % 5 == 0:
      adb_utils.issue_generic_request(
          [
              'shell', 'monkey', '-p', package,
              '-candroid.intent.category.LAUNCHER', '1',
          ],
          env.controller,
      )
    time.sleep(2.0)
    # Drive both the upstream flow and VLC 3.5.4's newer notification flow.
    # Re-check every round because these screens can appear late under
    # parallel cold-start load.
    _click_optional(
        env, controller,
        ('SKIP', 'Skip', 'GRANT PERMISSION', 'OK',
         'Allow access to manage all files', 'Allow'),
        rounds=1,
    )
    time.sleep(2.0)
  raise RuntimeError(f'VLC setup did not create {database}.')


def _configure_download_cache() -> None:
  """Use preloaded APKs while keeping AndroidWorld's canonical downloader."""
  cache_value = os.environ.get('ANDROID_WORLD_DOWNLOAD_CACHE_DIR')
  if not cache_value:
    return
  cache_dir = Path(cache_value)
  if getattr(setup_apps.download_app_data, '_pi_gui_cached', False):
    return
  original_app_download = setup_apps.download_app_data
  original_forwarder_download = a11y_grpc_wrapper._get_accessibility_forwarder_apk

  def download_app(file_name: str) -> str:
    cached = cache_dir / file_name
    return str(cached) if cached.is_file() and cached.stat().st_size else original_app_download(file_name)

  def download_forwarder() -> bytes:
    cached = cache_dir / 'accessibility_forwarder.apk'
    return cached.read_bytes() if cached.is_file() and cached.stat().st_size else original_forwarder_download()

  download_app._pi_gui_cached = True
  setup_apps.download_app_data = download_app
  a11y_grpc_wrapper._get_accessibility_forwarder_apk = download_forwarder


def _stabilize_contacts_setup() -> None:
  """Handle optional Contacts onboarding and verify the contact editor."""
  contacts_class = setup_apps.ContactsApp
  if getattr(contacts_class.setup, '_pi_gui_verified', False):
    return

  def setup(cls, env) -> None:
    package = cls.package_name()
    adb_utils.clear_app_data(package, env.controller)
    adb_utils.launch_app(cls.app_name, env.controller)
    controller = tools.AndroidToolController(env=env.controller)
    try:
      _click_optional(
          env, controller, ('Skip', "Don't allow", 'Not now', 'Got it', 'OK'),
      )

      intent = (
          'am start -a android.intent.action.INSERT '
          '-t vnd.android.cursor.dir/contact'
      )
      for _ in range(5):
        adb_utils.issue_generic_request(['shell', intent], env.controller)
        time.sleep(2.0)
        state = env.get_state(wait_to_stabilize=False)
        labels = {
            value.strip().casefold()
            for element in state.ui_elements
            for value in (element.text, element.content_description)
            if value and value.strip()
        }
        if 'save' in labels:
          adb_utils.press_back_button(env.controller)
          time.sleep(1.0)
          adb_utils.press_back_button(env.controller)
          return
        _click_optional(
            env, controller,
            ('Skip', "Don't allow", 'Not now', 'Got it', 'OK'), rounds=1,
        )
      raise RuntimeError('Contacts setup did not reach an editor with a Save action.')
    finally:
      adb_utils.close_app(cls.app_name, env.controller)

  setup._pi_gui_verified = True
  contacts_class.setup = classmethod(setup)


def _click_optional(
    env, controller, labels: tuple[str, ...], rounds: int = 8,
) -> None:
  for _ in range(rounds):
    time.sleep(1.0)
    state = env.get_state(wait_to_stabilize=False)
    visible = {
        element.text.strip().casefold()
        for element in state.ui_elements
        if element.text and element.text.strip()
    }
    label = next(
        (value for value in labels if value.casefold() in visible), None,
    )
    if label is not None:
      controller.click_element(label)
      time.sleep(1.0)


def _stabilize_camera_setup() -> None:
  """Allow Camera onboarding to be absent or delayed."""
  camera_class = setup_apps.CameraApp
  if getattr(camera_class.setup, '_pi_gui_verified', False):
    return

  def setup(cls, env) -> None:
    package = cls.package_name()
    adb_utils.clear_app_data(package, env.controller)
    try:
      adb_utils.grant_permissions(
          package, 'android.permission.ACCESS_COARSE_LOCATION', env.controller,
      )
    except Exception:  # Permission availability varies with the system image.
      pass
    adb_utils.launch_app(cls.app_name, env.controller)
    try:
      controller = tools.AndroidToolController(env=env.controller)
      _click_optional(
          env, controller, ('NEXT', 'Next', 'Continue', 'Got it', 'OK'),
      )
    finally:
      adb_utils.close_app(cls.app_name, env.controller)

  setup._pi_gui_verified = True
  camera_class.setup = classmethod(setup)


