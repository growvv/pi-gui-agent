def parse_args() -> argparse.Namespace:
  parser = argparse.ArgumentParser(description=__doc__)
  parser.add_argument('config', help='worker JSON generated by the parallel runner')
  return parser.parse_args()


class AndroidWorldHttpClient:

  def __init__(self, base_url: str, timeout: float = 120.0) -> None:
    self.base_url = base_url.rstrip('/')
    self.timeout = timeout

  def get(self, path: str, **params: Any) -> dict[str, Any]:
    return self._request('GET', path, params=params)

  def post(
      self, path: str, *, body: dict[str, Any] | None = None, **params: Any,
  ) -> dict[str, Any]:
    return self._request('POST', path, params=params, body=body)

  def wait_until_healthy(self, timeout: float = 600.0) -> None:
    deadline = time.monotonic() + timeout
    last_error: Exception | None = None
    while time.monotonic() < deadline:
      try:
        if self.get('/health').get('status') == 'success':
          return
      except Exception as caught:  # pylint: disable=broad-exception-caught
        last_error = caught
      time.sleep(2.0)
    raise TimeoutError(f'AndroidWorld server did not become healthy: {last_error}')

  def _request(
      self, method: str, path: str, *, params: dict[str, Any],
      body: dict[str, Any] | None = None,
  ) -> dict[str, Any]:
    query = parse.urlencode(params)
    url = f'{self.base_url}{path}' + (f'?{query}' if query else '')
    data = json.dumps(body).encode() if body is not None else None
    headers = {'Content-Type': 'application/json'} if data is not None else {}
    try:
      with request.urlopen(
          request.Request(url, data=data, headers=headers, method=method),
          timeout=self.timeout,
      ) as response:
        return json.loads(response.read())
    except error.HTTPError as caught:
      detail = caught.read().decode(errors='replace')
      raise RuntimeError(f'{method} {path} returned HTTP {caught.code}: {detail}') from caught


def main() -> None:
  config = load_worker_config(parse_args().config)
  if config.suite.transport != 'fastapi':
    raise ValueError('http_run requires suite.transport = "fastapi"')
  _prepare_device(config)
  runtime = config.runtime
  log_path = Path(runtime.checkpoint_dir).parent / 'server.log'
  with log_path.open('w', encoding='utf8') as server_log:
    server = subprocess.Popen(
        [
            'python3', '-m', 'uvicorn',
            'experiments.androidworld.http_server:app',
            '--host', '127.0.0.1', '--port', '5000',
        ],
        stdout=server_log, stderr=subprocess.STDOUT, text=True,
    )
    try:
      client = AndroidWorldHttpClient(runtime.server_url)
      client.wait_until_healthy()
      _run_suite(config, client)
    finally:
      server.terminate()
      try:
        server.wait(timeout=30)
      except subprocess.TimeoutExpired:
        server.kill()
        server.wait()


def _prepare_device(config) -> None:
  """Install AndroidWorld apps before the upstream FastAPI server attaches."""
  _increase_activity_start_timeout()
  _configure_download_cache()
  _stabilize_camera_setup()
  _stabilize_chrome_setup()
  _stabilize_clipper_setup()
  _stabilize_contacts_setup()
  _stabilize_simple_sms_setup()
  _stabilize_vlc_setup()
  environment = env_launcher.load_and_setup_env(
      console_port=config.runtime.console_port,
      emulator_setup=True,
      adb_path=config.runtime.adb_path,
      grpc_port=config.runtime.grpc_port,
  )
  environment.close()


def _run_suite(config, client: AndroidWorldHttpClient) -> None:
  settings = config.suite
  max_steps = getattr(config.agent, 'max_steps', 100)
  client.get(
      '/suite/reinitialize', n_task_combinations=settings.combinations,
      seed=settings.seed, task_family=settings.family,
  )
  checkpoint = checkpointer.IncrementalCheckpointer(config.runtime.checkpoint_dir)
  state_path = Path(config.runtime.checkpoint_dir).parent / 'state.json'
  episodes = []
  write_worker_state(
      state_path, settings.tasks, settings.combinations, episodes,
      max_steps, 'running',
  )
  try:
    for task_name in settings.tasks:
      task_episodes = []
      length = int(client.get('/suite/task_length', task_type=task_name)['length'])
      if length != settings.combinations:
        raise RuntimeError(
            f'{task_name} has {length} instances, expected {settings.combinations}'
        )
      for task_index in range(length):
        episode = _run_task(config, client, task_name, task_index)
        task_episodes.append(episode)
        episodes.append(episode)
        write_worker_state(
            state_path, settings.tasks, settings.combinations, episodes,
            max_steps, 'running',
        )
      checkpoint.save_episodes(task_episodes, f'{task_name}_0')
  except Exception:
    write_worker_state(
        state_path, settings.tasks, settings.combinations, episodes,
        max_steps, 'failed',
    )
    raise
  write_worker_state(
      state_path, settings.tasks, settings.combinations, episodes,
      max_steps, 'completed',
  )
  suite_utils.process_episodes(episodes, print_summary=True)


def _run_task(config, client, task_name: str, task_index: int) -> dict[str, Any]:
  started = time.monotonic()
  goal = client.get('/task/goal', task_type=task_name, task_idx=task_index)['goal']
  exception_info = None
  result_data: dict[str, Any] = {}
  successful = 0.0
  initialized = False
  try:
    print(f'Running task: {task_name}', flush=True)
    client.post('/task/initialize', task_type=task_name, task_idx=task_index)
    initialized = True
    agent_env = types.SimpleNamespace(interaction_cache='')
    agent = create_agent(
        config.agent.name,
        agent_env,
        workspace_dir=config.runtime.workspace_dir,
        timeout_seconds=config.suite.timeout_seconds,
        thinking=config.agent.thinking,
        settle_ms=config.suite.settle_ms,
        session_dir=config.runtime.session_dir,
        action_budget_multiplier=config.suite.action_budget_multiplier,
        min_actions=config.suite.min_actions,
        max_model_tokens=config.suite.max_model_tokens,
        learning=config.agent.learning,
        max_steps=getattr(config.agent, 'max_steps', 100),
        provider=config.agent.provider,
        model=config.agent.model,
        openclaw_model=config.agent.openclaw_model,
        enable_ledger_tool=config.agent.enable_ledger_tool,
        disable_ledger_tool=config.agent.disable_ledger_tool,
        server_url=config.runtime.server_url,
    )
    interaction = agent.step(goal)
    result_data = interaction.data or {}
    answer = getattr(agent_env, 'interaction_cache', '')
    if answer:
      client.post('/execute_action', body={'action_type': 'answer', 'text': answer})
    successful = float(client.get(
        '/task/score', task_type=task_name, task_idx=task_index,
    )['score'] or 0.0)
    if not interaction.done:
      successful = 0.0
  except Exception:  # pylint: disable=broad-exception-caught
    exception_info = traceback.format_exc()
    print(f'SKIPPING {task_name}.\n{exception_info}', flush=True)
  finally:
    if initialized:
      try:
        client.post('/task/tear_down', task_type=task_name, task_idx=task_index)
      except Exception:  # pylint: disable=broad-exception-caught
        if exception_info is None:
          exception_info = traceback.format_exc()
  return {
      constants.EpisodeConstants.GOAL: goal,
      constants.EpisodeConstants.TASK_TEMPLATE: task_name,
      constants.EpisodeConstants.INSTANCE_ID: task_index,
      constants.EpisodeConstants.EPISODE_DATA: {
          constants.STEP_NUMBER: [1], 'agent_result': [result_data],
      },
      constants.EpisodeConstants.IS_SUCCESSFUL: successful,
      constants.EpisodeConstants.RUN_TIME: time.monotonic() - started,
      constants.EpisodeConstants.FINISH_DTIME: datetime.datetime.now(),
      constants.EpisodeConstants.EPISODE_LENGTH: 1,
      constants.EpisodeConstants.AUX_DATA: {},
      constants.EpisodeConstants.SCREEN_CONFIG: 'portrait',
      constants.EpisodeConstants.EXCEPTION_INFO: exception_info,
      constants.EpisodeConstants.SEED: config.suite.seed,
      constants.EpisodeConstants.AGENT_NAME: config.agent.name,
  }


if __name__ == '__main__':
  main()
